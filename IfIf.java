class IfIf {
  public static void main(String[] args) {
    int temperature = -5;
    if(temperature > 0) {
      System.out.println("Temperature is positive");
    }
    else if(temperature == 0) {
      System.out.println("Temperature is zero");
    }
    else {
      System.out.println("Temperature is negative");
    }
  }
}